		<h2 class="jdbStage__subtitle">JOUR 16 - 22/02</h2>
		<p>Pour commencer cette semaine, on a cherché, avec Benjamin, plusieurs thèmes qui pourraient correspondre aux critères défini par les personnes chargées du projet et par le patron de la société. J’en ai sélectionné 4 et Benjamin 3. Hors de tout cela, on en a sélectionnés 3 puis, on les a proposés au responsable du projet qui a donc choisi le thème… . Ce thème que j’avais trouvé sur le site <a href="http://themeforest.net">themeforest.net</a> correspondait bien à nos attentes tant pour son design que pour le grand nombre de possibilités qu’il proposait, ce qui lui vaut un grand nombre de bons commentaires et une bonnes notes.</p>
		<p>Pour le reste de la journée, j’ai ré-installé tout et configuré les pages pour suivre le mockup de Benjamin. J’ai donc lu la documentation du thème et configuré le thème pour qu’il corresponde au mieux à nos envies. Je peux d’ailleurs dire que j’ai bien fait de demander à ce qu’on change de thème, car celui-ci est vraiment plus complet et mieux fini que le précédent. Il propose un vaste choix de possibilités de style, d’outils, de contenus et de personnalisations.</p>
		<p>Ça m’a donc pris toute la journée de mettre en place correctement ce thème (et la rapidité de l’ordinateur avec l’utilisation d’un gros thème sous Wordpress ne m’a franchement pas aidé à aller plus vite). En fin de journée, Aurélie, la secrétaire de direction m’a ajoutée un autre projet en rapport avec Évolution, à savoir d’ajouter de nouveau modules pour move international (après move Liège et Visé).</p>
		<p>J’aurais probablement fini la préparation du site demain dans l’après-midi et donc il sera prêt pour être mis en ligne sur le serveur pour y injecter du contenu. Je ferais l’intégration des modules après cela.</p>
