	<h1 class="projectTitle-1 title2">Stage - Semaine 8</h1>
	<div class="centerZone jdbStageDay jdbStageDay--lun jour1">
		<h2 class="jdbStage__subtitle">JOUR 36 - 21/03</h2>
		<p>J’ai commencé cette journée en corrigeant plusieurs éléments en rapport aux cookies de la semaine dernière. En premier lieux, j’ai changé sur chaque site les informations comme l’adresse email ou encore le titre principal. Ensuite, j’ai eu des changements ajoutés par Aurélie et Sophie qui m’ont amené à refaire une troisième fois le travail d’une autre façon mais en changeant le html (en nettoyant un peu le code que j’avais codé) et en effectuant des traductions qui s’affichent correctement car en le traduisant depuis l’onglet traduction de l’interface, je traduisais bien le texte mais le message ne s’affichait pas dans les autres langues.</p>
		<p>Bref, je me suis littéralement pris la tête avec ces cookies à un point que je n’en mangerai plus pendant un petit moment (enfin un cours moment car j’aime bien ça mais bon).</p>
		<a class="image-popup-no-margins" href="./img/cookie.gif">
			<img class="style__img--center style__img--middleSize" src="./img/cookie.gif" alt="image de cookies">
		</a>
		<p>J’ai donc pris une bonne partie de la journée pour refaire les sites dans plusieurs langues en changeant les informations et j’ai eu un autre site où je devais ajouter le module puis enfin, j’ai fait plusieurs séries de vérifications afin de m’assurer que tout fonctionnait comme on le voulait.</p>
		<p>Ensuite, j’ai dû revenir sur le logo du footer pour <a href="http://artisandupiano.be">le site de l’artisan du piano</a> pour ajouter un lien externe (target:_blank) puis j’ai regardé avec Benjamin pour un plugin qui permet de bien gérer les sitemap pour
			<a href="http://djm-mobile.be">le site de djm-mobile.be</a>. D’ailleurs, on a remarqué que le site est bien mieux positionné sur les moteurs de recherche depuis les quelques changements liés au référencement des derniers jours.</p>
		<p>Enfin, j’ai travaillé sur quelques visuels pour <a href="http://djmsport.djm.eu">le site de djm sport</a> mais en fin de journée, Sophie m’a donnée un nouveau projet Wordpress pour un client où il va dans un premier temps s’agir de trouver un bon thème pour les besoins de client et dans un second temps, le mettre en place. Donc Thomas m’a annoncé fin de journée que c’est Élise qui va reprendre le design du
			<a href="http://djmsport.djm.eu">site djm sport</a>.</p>
	</div>

	<div class="centerZone jdbStageDay jdbStageDay--mar jour2">
		<h2 class="jdbStage__subtitle">JOUR 37 - 22/03</h2>
		<p>Aujourd’hui, j’ai travaillé sur la mise en place de <a href="http://emailing.djmweb.be/t/ViewEmail/r/E3A25693019EDE732540EF23F30FEDED">la newsletter (AIHE)</a> n’ayant pas eu de retour suite au mail à la responsable de projets Web. J’ai donc codé suivant les restrictions que les newsletter imposent et suivant les recommandations de Compaign Monitor ayant un peu plus l’habitude de cela. Je dois dire que j’aime encore bien faire les newsletter, je n’en ai pas trop mais à chaque fois, je trouve ça agréable à faire.</p>
		<p>Plus tard dans la journée, j’ai changé la phrase pour les Cookies pour les sites Wordpress et j’ai récupéré les plugins qu’on avait installés en ligne des sites Wordpress (pour Djm mobile, Djm sport et la siroperie d’Aubel) afin de les ajouter dans le Git du projet.</p>
	</div>

	<div class="centerZone jdbStageDay jdbStageDay--mer jour3">
		<h2 class="jdbStage__subtitle">JOUR 38 - 23/03</h2>
		<p>Aujourd’hui, j’ai commencé la journée en cherchant un meilleur plugin pour l’inscription à la newsletter et finalement, au bout de mes recherches, j’ai décidé d’utiliser celui qui est directement proposé par mailchimp. Il est un peu plus dodu mais il est très agréable et facile à styliser par-dessus.</p>
		<p>Ensuite, je l’ai installé, tester et styliser pour lui donner un rendu plus agréable.</p>
		<p>Après cela, j’ai corrigé <a href="http://emailing.djmweb.be/t/ViewEmail/r/E3A25693019EDE732540EF23F30FEDED">la newsletter d'AIHE</a> suite à de petites adaptations demandés par la cheffe de projets Web et de petites erreurs d’affichage remarquées après mes tests et vérifications sur Outlook et Gmail essentiellement.</p>
		<p>J’ai fait toute une série de tests pour vérifier que ça s’affiche bien et que qu’on puisse éditer la newsletter correctement. D’ailleurs, j’avais un problème un moment donné qui avait comme résultat de m’afficher correctement la newsletter mais pas de l’éditer (un peu ennuyeux) et après un moment de recherche en supprimant des éléments au fur et à mesure et tester si ça fonctionnait après, j’ai trouvé ma petite erreur. En réalité, j’avais mis un « ; » un attribut de style d’une balise comme width. Et oui, les habitudes de CSS ne sont pas toujours de bonne aide pour les newsletter.</p>
		<p>Enfin j’ai fini par trouver le problème, le résoudre et finir tous mes projets en cours dans JIRA pour la fin de journée.</p>
	</div>

	<div class="centerZone jdbStageDay jdbStageDay--jeu jour4">
		<h2 class="jdbStage__subtitle">JOUR 39 - 24/03</h2>
		<p>Aujourd’hui, pas de stage mais une petite rencontre avec des professionnels du milieu au campus 2000. C’était bien sympa et intéressant.</p>
	</div>

	<div class="centerZone jdbStageDay jdbStageDay--ven jour5">
		<h2 class="jdbStage__subtitle">JOUR 40 - 25/03</h2>
		<p>Aujourd’hui, j’ai commencé la journée dans les bouchons à cause d’une route bloquée (évidement celle que je prenais). Du coup, j’ai dû aller jusqu’à Barchon et puis revenir sur visé dans un très long bouchon.</p>
		<p>Une fois chez djm, je suis allé voir Sophie (la responsable de projet Web) pour discuter du site d’Élite soccer que je vais devoir réaliser d’ici peu car j’avais fait des recherches mais je n’avais pas eu de retour concret sur le projet. Elle m’a dit que je pourrai commencer début de semaine prochaine car ils n’ont pas encore communiqué toutes les informations indispensables (comme le logo et d’autres choses du genre).</p>
		<p>Après, je suis allé travailler sur <a href="http://djmsport.djm.eu">le site de djm sport</a> et celui de <a
				href="http://oali.be">Oali</a> pour d’une part mettre en place un formulaire de contact, améliorer le design et d’autre part pour mettre en  place de façon définitive le formulaire d’inscription à la newsletter.</p>
		<p>J’ai donc corrigé quelques éléments de design qu’une des graphiste avait fait et qui ne plaisait pas vraiment à Thomas. J’ai changé et adapté le design par-ci par là et changé quelques images puis j’ai refait le formulaire de contact sur base de celui de
			<a href="http://djmsport.sjm.eu">djm sport</a>. J’ai fait le css en less et j’ai testé le tout avant de faire valider.</p>
		<p>Pour Oali, j’ai testé sur <a href="http://sirop.djm.eu">le site de la siroperie</a> (qui sert un peu de site test ces temps-ci pour
			<a href="http://oali.be">celui de Quentin</a>) et une fois que Quentin a été satisfait, j’ai copié la structure du formulaire et le css que j’avais fait vers son site.</p>
		<p>En fin de journée, j’ai terminé les projets que j’avais à faire et il me reste donc le site d’Élite soccer pour la semaine prochaine.</p>
	</div>

	<div class="centerZone jdbStageDay jdbStageDay--summary">
		<h2 class="jdbStage__subtitle">Résumé de la semaine&nbsp;:</h2>
		<p>40 JOURS&nbsp;! et  oui, 40 de passé chez djm digital et surtout, dans un petit mois c’est déjà fini&nbsp;!</p>
		<p>Cette semaine j’ai travaillé essentiellement sur les finitions de la mise en place massive de messages informant l’internaute de l’utilisation de cookies, une recherche de thème pour un nouveau site de location de terrains de sport (Élite Soccer), une newsletter (AIHE), le plugin de newsletter pour le site de la siroperie et celui de Oali et enfin, quelques améliorations au design du site djm sport.</p>
		<p>Par ailleurs, j’ai aussi été au Job Day ce jeudi ce qui a fait une petite pause dans la semaine et qui m’a permis de rencontrer des professionnels du métier et de discuter avec eux d’emplois potentiels.</p>
