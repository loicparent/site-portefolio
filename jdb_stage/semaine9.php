	<div class="centerZone jdbStageDay jdbStageDay--summary">
		<h2 class="jdbStage__subtitle">Résumé de la semaine&nbsp;:</h2>
		<p>Cette semaine a commencé par un jour de congé (que demander de plus ^^). Plus sérieusement, j’ai travaillé principalement sur une newsletter (AIHE) et un nouveau site avec Wordpress (Élite Soccer).</p>
		<p>J’ai donc encore eu l’occasion de faire une newsletter et ça ne me dérange pas du tout car je dois dire que c’est assez sympa à faire en fin de compte malgré le fait de devoir codé de façon archaïque.</p>
		<p>Pour le site d’Élite Soccer, j’ai dû choisir un thème adapté au client, l’installer/configurer et intégrer selon une architecture plus ou moins précise.</p>
		<p>J’ai eu l’occasion de découvrir un autre plugin qui ressemble à Visual Composer (mais en moins bien), appelé Page Buider. J’ai été un peu embêté par plusieurs choses avec cet outil comme le fait qu’on puisse créer des colonnes (jusque là tout va bien) mais une fois qu’on veut, pour une raison X ou Y, changer et revenir sur une simple rangée, et bien on ne peux pas et donc on doit refaire ce qu’on avait déjà fait. En dehors de ce petit problème (et d’autres petits), le thème était relativement complet, tout s’est bien passé.</p>
